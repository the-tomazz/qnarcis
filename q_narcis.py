    def onFinishedCheckForQgzVersion(self, task):
        def _finish_check_without_install():
            self.task = None
            self.enableActions()
            if self.queue:
                self.queue()
                self.queue = None

        s = QgsSettings()
        clientVersionIdRaw = s.value('q_narcis/qgz_version', None)
        clientVersionId = _normalize_qgz_version(clientVersionIdRaw)
        if clientVersionIdRaw is not None and clientVersionId is None:
            QgsMessageLog.logMessage(
                f"Invalid q_narcis/qgz_version in settings, clearing it: {clientVersionIdRaw!r}",
                "QNarcIS",
                Qgis.Warning,
            )
            s.remove('q_narcis/qgz_version')

        if not hasattr(task, 'taskFunResult') or task.taskFunResult == None:
            self.iface.messageBar().pushMessage(self.tr("Povezovanje na NarcIS strežnik"), self.tr("Napaka pri povezavi na https://narcis.gov.si/ords/narcis/hr/qgz"), level=Qgis.Warning)
            _finish_check_without_install()
            return
        elif len(task.taskFunResult) == 0:
            self.iface.messageBar().pushMessage(self.tr("Seznam slojev"), self.tr("Seznam slojev za državo") + " " + self.country_code + " " + self.tr("je prazen") , level=Qgis.Warning)
            _finish_check_without_install()
            return
        
        id = task.taskFunResult['id']
        normalized_server_id = _normalize_qgz_version(id)
        if normalized_server_id is None:
            QgsMessageLog.logMessage(
                f"Invalid catalog id from server, aborting install: {id!r}",
                "QNarcIS",
                Qgis.Critical,
            )
            self.iface.messageBar().pushMessage(
                self.tr("Nameščanje kataloga slojev"),
                self.tr("Napaka pri nameščanju kataloga slojev"),
                level=Qgis.Critical
            )
            _finish_check_without_install()
            return
        url = task.taskFunResult['povezava']
        drzava = task.taskFunResult['drzava']
        callback = task.taskFunResult['callback']
        sub_folder = task.taskFunResult['sub_folder']

        url = url.strip()
        if url.lower().endswith('.qgz'):
            url = url[:-4] + '.tar.xz'

        if not clientVersionId or clientVersionId != normalized_server_id or sub_folder:
            self.task = QNarcisTask(self.tr("Nameščanje novega kataloga slojev" if not sub_folder else "Pripravljanje kataloga slojev"), installQgz,[normalized_server_id, url, self.plugin_dir, drzava, callback, sub_folder], self.onFinishedInstallingQgz)
            QgsApplication.taskManager().addTask( self.task )
            return

        if not sub_folder:
            self.qgz_version = normalized_server_id
        
        self.task = None
        self.enableActions()

