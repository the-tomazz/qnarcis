    def insertGroup(self, group, groupName, groupIndex):
        for g in group.children():
            if isinstance(g, QgsLayerTreeGroup) and g.name() == groupName:
                return g
                
        pos = self.getLayerTreeItemPosition(group, groupIndex)
                
        g = group.insertGroup(pos, groupName)
        self.layerTreeIndexByTreeItemId[id(g)] = groupIndex

        return g

    def getLayerTreeItemPosition(self, group, itemInx):
        pos = 0
        for child in group.children():
            
            if isinstance(child, QgsLayerTreeLayer):
                treeItemId = child.layer().id()
            else:
                treeItemId = id(child)
            
            if treeItemId in self.layerTreeIndexByTreeItemId:
                cinx = self.layerTreeIndexByTreeItemId[treeItemId]
                if cinx > itemInx:
                    break
            pos = pos + 1
        return pos

