    def saveCredentialsIfValidAsync(self, uname, pwd, cfg):
        """
        If credentials came from the dialog (cfg is None), validate them in a background task
        using QGIS networking. On success, persist them via addConfig(uname, pwd). Silent on failure.
        """
        if cfg is not None:
            return

        def _check(un, pw):
            try:
                url = "https://narcis.gov.si/ords/narcis/qnarcis-protected/get-user/"
                req = QNetworkRequest(QUrl(url))
                req.setRawHeader(b"uporabnik", un.encode("utf-8"))
                req.setRawHeader(b"key", pw.encode("utf-8"))

                br = QgsBlockingNetworkRequest()
                status = br.get(req)
                if status == QgsBlockingNetworkRequest.NoError:
                    reply = br.reply()
                    data = reply.content().data()
                    try:
                        resp = json.loads(data.decode("utf-8", errors="ignore"))
                    except Exception:
                        resp = {}
                    return "kljuc" in resp
            except Exception:
                pass
            return False

        def _on_finished(task):
            ok = getattr(task, "taskFunResult", False)
            if ok:
                try:
                    self.addConfig(uname, pwd)
                except Exception:
                    pass

        QgsApplication.taskManager().addTask(
            QNarcisTask(self.tr("Preverjanje prijave"), _check, [uname, pwd], _on_finished)
        )

    def getCredentials(self):
        am  = QgsApplication.authManager()
        uname = None
        pwd = None
        cfg = None

        if 'qnarcis_oauth' not in am.availableAuthMethodConfigs().keys():
            dialog = CustomCredentialsDialog()
            if dialog.exec_() == QDialog.Accepted:
                uname, pwd = dialog.get_credentials()
                uname = uname.strip()
                pwd = pwd.strip()
        else:       
            cfg = QgsAuthMethodConfig()
            (success,dummy)=am.loadAuthenticationConfig('qnarcis_oauth', cfg, True)
            if success:
                cmap = cfg.configMap()
                uname = cmap['username']
                pwd = cmap['password']
        
        return uname, pwd, cfg

