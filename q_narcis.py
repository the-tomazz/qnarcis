    def _read_layer_name_list_setting(self, settings_obj, key):
        raw_value = settings_obj.value(key, None)
        if raw_value is None:
            return []

        if isinstance(raw_value, list):
            return [str(v) for v in raw_value]

        if isinstance(raw_value, str):
            raw_text = raw_value.strip()
            if not raw_text:
                return []
            try:
                parsed = json.loads(raw_text)
                if isinstance(parsed, list):
                    return [str(v) for v in parsed]
            except Exception:
                pass
            return [raw_text]

        return []

    def _read_bool_setting(self, settings_obj, key, default=False):
        raw_value = settings_obj.value(key, None)
        if raw_value is None:
            return bool(default)
        if isinstance(raw_value, bool):
            return raw_value
        if isinstance(raw_value, (int, float)):
            return bool(raw_value)
        text = str(raw_value).strip().lower()
        if text in ('1', 'true', 'yes', 'y', 'on'):
            return True
        if text in ('0', 'false', 'no', 'n', 'off', ''):
            return False
        return bool(default)

    def _normalize_layer_name_list(self, values):
        result = []
        seen = set()
        for value in values or []:
            item = str(value).strip()
            if item and item not in seen:
                result.append(item)
                seen.add(item)
        return result

    def _normalize_layer_title(self, value):
        text = str(value or '')
        for dash in ('\u2010', '\u2011', '\u2012', '\u2013', '\u2014', '\u2212'):
            text = text.replace(dash, '-')
        return ' '.join(text.split()).strip().casefold()

    def _normalize_identity_layer_name(self, value):
        return str(value or '').replace('🔒', '').strip()

    def _build_layer_identity_key(self, provider_key, source, layer_name, ancestor_names, locked):
        provider = str(provider_key or '').strip().upper()
        type_name = extract_typename(source or '', provider_key or '') or ''
        normalized_name = self._normalize_layer_title(self._normalize_identity_layer_name(layer_name))
        normalized_ancestors = ".".join(self._normalize_layer_title(name) for name in (ancestor_names or []))
        lock_flag = '1' if bool(locked) else '0'
        return f"{provider}|{type_name}|{normalized_ancestors}|{normalized_name}|{lock_flag}"

    def _register_catalog_layer_identity(self, layer_item_data):
        if not layer_item_data:
            return

        xmlitem = layer_item_data.get('xmlitem')
        layer_name = xmlitem.get('name') if xmlitem is not None else ''
        ancestor_names = [a.get('name') for a in layer_item_data.get('ancestors', [])]
        key = self._build_layer_identity_key(
            layer_item_data.get('providerKey'),
            layer_item_data.get('source'),
            layer_name,
            ancestor_names,
            layer_item_data.get('locked'),
        )
        layer_item_data['identity_key'] = key
        self.layers_by_identity.setdefault(key, []).append(layer_item_data)

    def _resolve_catalog_layer_definition_for_project_layer(self, project_layer, layer_name, ancestor_names, prefer_locked=False):
        provider_key = project_layer.providerType()
        source = project_layer.source()
        project_has_authcfg = 'authcfg' in (source or '')

        lock_modes = [bool(prefer_locked), not bool(prefer_locked)]
        for lock_mode in lock_modes:
            key = self._build_layer_identity_key(provider_key, source, layer_name, ancestor_names, lock_mode)
            candidates = self.layers_by_identity.get(key, [])
            if candidates:
                if len(candidates) > 1:
                    matched_authcfg = [
                        candidate for candidate in candidates
                        if ('authcfg' in (candidate.get('source') or '')) == project_has_authcfg
                    ]
                    if matched_authcfg:
                        return matched_authcfg[0]
                return candidates[0]

        # Backward-compatible fallback by catalog name.
        if layer_name in self.layers:
            layer_definition = self.layers[layer_name]
            layer_definition_ancestors = [a.get('name') for a in layer_definition.get('ancestors', [])]
            if ".".join(ancestor_names) == ".".join(layer_definition_ancestors):
                return layer_definition

        return None

    def _iter_leaf_layer_names_in_model(self, model, parent=QModelIndex()):
        names = []
        row_count = model.rowCount(parent)
        for row in range(row_count):
            index = model.index(row, 0, parent)
            layer_name = model.data(index, Qt.DisplayRole)
            qgz_layer_id = model.data(index, Qt.UserRole + 1)
            if qgz_layer_id and layer_name:
                names.append(str(layer_name))
            if model.hasChildren(index):
                names.extend(self._iter_leaf_layer_names_in_model(model, index))
        return names

    def _find_layer_name_in_model(self, model, preferred_names):
        preferred = self._normalize_layer_name_list(preferred_names)
        for name in preferred:
            items = model.findItems(name, Qt.MatchRecursive | Qt.MatchExactly)
            if items:
                return items[0].text()

        normalized_preferred = {self._normalize_layer_title(name): name for name in preferred}
        for model_name in self._iter_leaf_layer_names_in_model(model):
            if self._normalize_layer_title(model_name) in normalized_preferred:
                return model_name
        return None

    def _find_layer_name_in_catalog(self, preferred_names):
        preferred = self._normalize_layer_name_list(preferred_names)
        for name in preferred:
            if name in self.layers:
                return name

        normalized_preferred = {self._normalize_layer_title(name): name for name in preferred}
        for layer_name in self.layers.keys():
            if self._normalize_layer_title(layer_name) in normalized_preferred:
                return layer_name
        return None

    def _get_country_default_layer_names(self, country_code=None, model=None):
        country = country_code or self.country_code
        if country != 'SI':
            default_name = 'OpenStreetMap'
            if model is not None:
                found = self._find_layer_name_in_model(model, [default_name])
                return [found] if found else []
            found = self._find_layer_name_in_catalog([default_name])
            return [found] if found else [default_name]

        # Preferred SI startup order:
        # A: Pregledna topografska karta (MDP)
        # B: DOF (2023 - 2025) (GURS)
        # C: DOF 2022 - 2024 (ARSO)
        si_default_groups = [
            ['Pregledna topografska karta'],
            ['DOF (2023 - 2025)'],
            ['DOF 2022 - 2024', 'DOF 2022 – 2024']
        ]
        resolved = []
        for preferred_names in si_default_groups:
            if model is not None:
                found = self._find_layer_name_in_model(model, preferred_names)
            else:
                found = self._find_layer_name_in_catalog(preferred_names)
            if found:
                resolved.append(found)

        return self._normalize_layer_name_list(resolved)

    def _get_default_layer_candidates(self):
        startup_enabled = bool(getattr(self, 'default_layer_enabled', True))
        if not startup_enabled:
            return []

        candidates = self._normalize_layer_name_list(getattr(self, 'default_layer_names', []))

        if not candidates:
            candidates = self._get_country_default_layer_names(self.country_code)

        if self.default_layer_name:
            candidates = self._normalize_layer_name_list([self.default_layer_name] + candidates)

        return candidates

