def parseTree(aditionalDataByLayerId, xmlParent, node):
    for child in node.children():
        if isinstance(child, QgsLayerTreeGroup) and len(child.children()) > 0:
            xmlChild = ET.SubElement(xmlParent, 'layer-tree-group', name=child.name())
            parseTree(aditionalDataByLayerId, xmlChild, child)
        elif isinstance(child, QgsLayerTreeLayer):
            lay = child.layer()
            if lay.isSpatial():
                id=child.layerId()
                name=child.name()
                source = lay.source()
                providerKey=lay.providerType()
                
                md = lay.metadata()
                contacts = md.contacts()
                keywords = md.keywords()
                links = md.links()
                constraints = md.constraints()

                stanje = ''
                organization = ''
                owner = ''
                url = ''
                description = ''
                locked = False

                for link in links:
                    if link.name == '_narcis_ext_link':
                        url = link.url
                        description = link.description
                        break

                if '_narcis_stanje' in keywords:
                    stanje = keywords['_narcis_stanje'][0]

                if len(contacts) > 0:
                    organization = contacts[0].organization
                    if len(contacts) > 1:
                        owner = contacts[1].organization
                
                for c in constraints:
                    if c.type == 'PASSWORD_PROTECTED' and c.constraint == 'YES':
                        locked = True
                        break

                aditionalDataByLayerId[id] = {'minScale': lay.minimumScale(), 'maxScale': lay.maximumScale(), 'organization': organization, 'owner': owner, 'stanje': stanje, 'url': url, 'description': description, 'locked': locked}
                xmlChild = ET.SubElement(xmlParent, 'layer-tree-layer', 
                    name=name,id=id,source=source,providerKey=providerKey)

def _normalize_qgz_version(value):
    """Return normalized non-negative integer version as string, otherwise None."""
    if value is None or isinstance(value, bool):
        return None

    if isinstance(value, int):
        return str(value) if value >= 0 else None

    if isinstance(value, float):
        if value >= 0 and value.is_integer():
            return str(int(value))
        return None

    text = str(value).strip()
    if not text or not re.fullmatch(r"\d+", text):
        return None

    return str(int(text))

def installQgz(id, url, plugin_dir, drzava, callback=None, sub_folder=''):
    try:
        def _validate_catalog_archive(archive_path):
            import tarfile
            with tarfile.open(archive_path, 'r:xz') as tar:
                names = []
                for member in tar.getmembers():
                    normalized = member.name.lstrip("./").strip("/")
                    if normalized:
                        names.append(normalized)

            if 'vsi_sloji.xml' not in names:
                raise RuntimeError("Catalog archive is missing vsi_sloji.xml")
            if 'additional_layer_data.json' not in names:
                raise RuntimeError("Catalog archive is missing additional_layer_data.json")
            if not any(name == 'qml' or name.startswith('qml/') for name in names):
                raise RuntimeError("Catalog archive is missing qml directory")

        def _download_catalog_archive(download_url, destination_path):
            tmp_path = destination_path + ".tmp"
            if os.path.exists(tmp_path):
                os.remove(tmp_path)

            try:
                r = requests.get(download_url, allow_redirects=True, timeout=60)
                status_code = getattr(r, "status_code", None)
                if status_code is not None and int(status_code) >= 400:
                    raise RuntimeError(f"Catalog download failed with HTTP status {status_code}")
                if not getattr(r, "content", None):
                    raise RuntimeError("Catalog download returned empty response body")

                with open(tmp_path, 'wb') as fileStream:
                    fileStream.write(r.content)
                os.replace(tmp_path, destination_path)
            except Exception:
                if os.path.exists(tmp_path):
                    os.remove(tmp_path)
                if os.path.exists(destination_path):
                    os.remove(destination_path)
                raise

        normalized_id = _normalize_qgz_version(id)
        if normalized_id is None:
            raise RuntimeError(f"Invalid catalog id received: {id!r}")

        if sub_folder:
            subFolderPath = os.path.join(plugin_dir, sub_folder)
            if not os.path.exists(subFolderPath):
                os.mkdir(subFolderPath)

        cachedFolderPath = os.path.join(plugin_dir, '_cached_qgz')
        if not os.path.exists(cachedFolderPath):
            os.mkdir(cachedFolderPath)

        cachedXzPath = os.path.join(cachedFolderPath, str(drzava) + '_' + normalized_id + '.tar.xz')
        downloadedXzPath = os.path.join(cachedFolderPath, 'qnarcis.tar.xz')

        import shutil
        archive_path = None
        downloaded_this_run = False

        if os.path.exists(downloadedXzPath):
            os.remove(downloadedXzPath)

        if os.path.exists(cachedXzPath):
            try:
                _validate_catalog_archive(cachedXzPath)
                archive_path = cachedXzPath
            except Exception as cache_exc:
                QgsMessageLog.logMessage(
                    f"Cached catalog archive invalid, re-downloading: {cache_exc}",
                    "QNarcIS",
                    Qgis.Warning,
                )
                try:
                    os.remove(cachedXzPath)
                except OSError as remove_exc:
                    QgsMessageLog.logMessage(
                        f"Failed to remove invalid cached archive '{cachedXzPath}': {remove_exc}",
                        "QNarcIS",
                        Qgis.Warning,
                    )

        if archive_path is None:
            _download_catalog_archive(url, downloadedXzPath)
            downloaded_this_run = True
            _validate_catalog_archive(downloadedXzPath)
            archive_path = downloadedXzPath

        if not archive_path or not os.path.exists(archive_path):
            return None
        
        # Clean up existing qml folder before extraction
        extract_path = os.path.join(plugin_dir, sub_folder) if sub_folder else plugin_dir
        qml_folder = os.path.join(extract_path, 'qml')
        if os.path.exists(qml_folder):
            shutil.rmtree(qml_folder)
        
        import tarfile
        with tarfile.open(archive_path, 'r:xz') as tar:
            extractall_kwargs = {}
            if sys.version_info >= (3, 12):
                extractall_kwargs["filter"] = "data"
            tar.extractall(extract_path, **extractall_kwargs)

        for required_item in ('vsi_sloji.xml', 'additional_layer_data.json', 'qml'):
            if not os.path.exists(os.path.join(extract_path, required_item)):
                raise RuntimeError(f"Catalog extraction failed: missing {required_item}")
        
        json_path = os.path.join(extract_path, 'additional_layer_data.json')
        if os.path.exists(json_path):
            with open(json_path, 'r') as f:
                additional_data = json.load(f)
            
            if '_extents' in additional_data:
                extents = additional_data['_extents']
                s = QSettings()
                for country_code, extent in extents.items():
                    s.setValue('q_narcis/default_extent/' + country_code, json.dumps(extent))
                
                del additional_data['_extents']
                with open(json_path, 'w') as f:
                    json.dump(additional_data, f, indent=2)
        
        if not sub_folder:
            s = QSettings()
            s.setValue('q_narcis/qgz_version', normalized_id)
        
        if downloaded_this_run:
            os.replace(downloadedXzPath, cachedXzPath)

    except Exception as exc:
        try:
            if 'downloadedXzPath' in locals():
                for path in (downloadedXzPath, downloadedXzPath + ".tmp"):
                    if os.path.exists(path):
                        os.remove(path)
        except Exception:
            pass
        QgsMessageLog.logMessage(
            "installQgz failed: "
            f"id={id}, drzava={drzava}, sub_folder='{sub_folder}', "
            f"url='{url}', error={exc}",
            "QNarcIS",
            Qgis.Critical,
        )
        QgsMessageLog.logMessage(traceback.format_exc(), "QNarcIS", Qgis.Critical)
        return None

    return {'id': normalized_id, 'callback': callback, 'sub_folder': sub_folder}

def urlFetchSync(url):
    request = QgsBlockingNetworkRequest()
    status = request.get(QNetworkRequest(QUrl(url)))
    if status == QgsBlockingNetworkRequest.NoError:
        reply = request.reply()
        content = reply.content()
        return json.loads(content.data().decode())
    return None

def format_decimal(number):
    if isinstance(number, str):
        number = float(number)
    # Check if the number has decimal places
    if int(number) == number:
        # If no decimal places, return without decimal point
        return "{:.0f}".format(number)
    else:
        # If decimal places exist, round to one decimal place
        rounded_number = round(number, 1)
        # Check if the first decimal place is 0 after rounding
        if int(rounded_number * 10) % 10 == 0:
            # If the first decimal place is 0, return the integer part
            return "{:.0f}".format(rounded_number)
        else:
            # Otherwise, return with one decimal place
            return "{:.1f}".format(rounded_number)

def extract_typename(source, provider_key):
    """
    Extracts the typename value from a source string.

    Args:
        source (str): The input string containing typename='...'

    Returns:
        str | None: The extracted typename value, or None if not found.
    """

    svc = provider_key.upper().strip()
    if svc == "WMS":
        parsed = parse_qs(source)
        layers = parsed.get("layers", [])
        return layers[0] if layers else None
    else:
        match = re.search(r"typename='([^']+)'", source)
        return match.group(1) if match else None

