    def apiLogin(self, skip_if_not_logged = False):
        am = QgsApplication.authManager()
        uname = None
        pwd = None
        cfg = None

        # 1. Check if qnarcis_oauth config exists
        if 'qnarcis_oauth' in am.availableAuthMethodConfigs().keys():
            cfg = QgsAuthMethodConfig()
            success, _ = am.loadAuthenticationConfig('qnarcis_oauth', cfg, True)
            if success:
                cmap = cfg.configMap()
                uname = cmap['username']
                pwd = cmap['password']
            else:
                self.iface.messageBar().pushMessage("Napaka", "Napaka pri nalaganju avtentikacijske konfiguracije qnarcis_oauth.", level=Qgis.Critical)
                return
        elif skip_if_not_logged:
            return
        else:
            # 2. Prompt for credentials
            dialog = CustomCredentialsDialog()
            if dialog.exec_() == QDialog.Accepted:
                uname, pwd = dialog.get_credentials()
                uname = uname.strip()
                pwd = pwd.strip()
            else:
                self.iface.messageBar().pushMessage("Prekinjeno", "Prijava je bila preklicana.", level=Qgis.Info)
                return

        auth_id, config_name, user_name = findGeoserverAuthConfig()
        if  auth_id:
            return
        # 3. Attempt login (regardless of source: config or dialog)
        
        url = "https://narcis.gov.si/ords/narcis/qnarcis-protected/get-user/"
        headers = {"uporabnik": uname, "key": pwd}

        try:
            r = requests.get(url, headers=headers, timeout=10)
            if r.status_code == 200:
                try:
                    resp_json = r.json()
                except Exception:
                    resp_json = {}

                if "kljuc" in resp_json:
                    if cfg is None:
                        self.addConfig(uname, pwd)

                    gsrv_user_name = resp_json['uporabnik']
                    
                    if gsrv_user_name != 'n/a':
                        self.geoserver_credentials = {
                            "key": resp_json['kljuc'],
                            "user": gsrv_user_name,
                            "qnarcis_user": uname
                        }

                        self.addGeoserverConfig(self.geoserver_credentials.get('user'), self.geoserver_credentials.get('key'))
                        
                        self.updateLoginStatusUI()
                        self.iface.messageBar().pushMessage("QNarcIS", "Prijava uspešna za: {}".format(uname), level=Qgis.Success)
                        return
            
             # Login failed: delete config if it existed
            if cfg is not None:
                am.removeAuthenticationConfig('qnarcis_oauth')

            self.iface.messageBar().pushMessage(
                "Napaka pri prijavi",
                "Nepravilni podatki ali ni dostopa.",
                level=Qgis.Critical
            )
        except Exception as e:
            self.iface.messageBar().pushMessage(
                "Napaka pri povezavi",
                "Ne morem se povezati s strežnikom: {}".format(str(e)),
                level=Qgis.Critical
            )

        self.geoserver_credentials = None
        self.updateLoginStatusUI()

