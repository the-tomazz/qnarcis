    def enumerateExistingLayers(self):
        self.layersAreEnumerated = False
        root = QgsProject.instance().layerTreeRoot()
        for layer in QgsProject.instance().mapLayers().values():
            rawLayerName = layer.name()
            layerName = self._normalize_identity_layer_name(rawLayerName)
            layerTreeItem = root.findLayer(layer.id())
            if layerTreeItem is None:
                continue
            layerTreeItemAncestors = []
            self.getLayerAncestorNames(layerTreeItem, layerTreeItemAncestors)
            
            if len(layerTreeItemAncestors) > 0:
                layerTreeItemAncestors.pop()
            
            ancestorNames = [a.name() for a in layerTreeItemAncestors]
            layerDefinition = self._resolve_catalog_layer_definition_for_project_layer(
                layer,
                layerName,
                ancestorNames,
                prefer_locked='🔒' in str(rawLayerName or '')
            )
            if not layerDefinition:
                continue

            layerDefinitionAncestorsNames = [a.get('name') for a in layerDefinition['ancestors']]
            
            if ".".join(ancestorNames) != ".".join(layerDefinitionAncestorsNames):
                continue

            layerDefinition['layer'] = layer
            self.layerTreeIndexByTreeItemId[layer.id()] = layerDefinition['xmlitem'].attrib['__ginx___']
            for inx, a in enumerate(layerDefinition['ancestors']):
                layerTreeItemAncestor = layerTreeItemAncestors[inx]
                self.layerTreeIndexByTreeItemId[id(layerTreeItemAncestor)] = a.attrib['__ginx___']
                a.attrib['__qgzgroup___'] = layerTreeItemAncestor

            modelIndex = self.getTreeViewModelIndex(layerName)
            
            treeItemColor = QColor(255,0,0)

            if layer.isValid():
                treeItemColor = QColor(46,139,87)
            elif layerName in self.locked_layers:
                auth_id = self.get_geoserver_credentials()
                if auth_id:
                    source = self.update_layer_source_authcfg(layer.source(), auth_id)
                    layer.setDataSource(source)

                    provider_key = layerDefinition['providerKey']
                    source_type_name = extract_typename(source, provider_key)

                    modelIndices = self.getTreeViewModelIndex(layerName, True)
                    modelLayerIds = [self.tree.data_model.data(modelIndex, Qt.UserRole+1) for modelIndex in modelIndices]

                    inx = next((i for i, k in enumerate(modelLayerIds) if k in self.locked_layers_by_id), None)

                    if inx:
                        modelIndex = modelIndices[inx]

                    for ld in self.locked_layers[layerName]:
                        type_name = ld.get('type_name') or extract_typename(ld.get('source'), provider_key)
                        ld['type_name'] = type_name
                        if type_name == source_type_name: #definition for project layer is found
                            ld['layer'] = layer

                    if layer.isValid():
                        treeItemColor = QColor(46,139,87)
            
            self.setTreeItemColorAndFont(treeItemColor, self.tree.data_model, modelIndex)
        self.layersAreEnumerated = True
    
