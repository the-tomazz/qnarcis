    def sendSelection(self):
        # prepreči zagon več vzporednih opravil pošiljanja
        if getattr(self, "_upload_running", False):
            self.iface.messageBar().pushMessage(
                "QNarcIS",
                "Pošiljanje je že v teku. Počakajte, da se trenutno opravilo zaključi.",
                level=Qgis.Info
            )
            return

        layer = self.iface.activeLayer()

        # 1) Preverjanje sloja in tipa
        if layer is None:
            self.iface.messageBar().pushMessage("Napaka", "Noben sloj ni aktiven.", level=Qgis.Warning)
            return
        if layer.type() is not QgsMapLayer.VectorLayer:
            self.iface.messageBar().pushMessage("Napaka", "Izbrati je treba VEKTORSKI (poligonski) sloj.", level=Qgis.Warning)
            return

        wkb = QgsWkbTypes.flatType(layer.wkbType())
        allowed = (
            QgsWkbTypes.Point, QgsWkbTypes.MultiPoint,
            QgsWkbTypes.LineString, QgsWkbTypes.MultiLineString,
            QgsWkbTypes.Polygon, QgsWkbTypes.MultiPolygon
        )
        if wkb not in allowed:
            self.iface.messageBar().pushMessage(
                "Napaka",
                "Izbrani sloj mora biti točkovni, linijski ali poligonski (vključno z Multi*).",
                level=Qgis.Warning
            )
            return

        # 1b) Opozorilo za CRS sloja (preveri CRS sloja, ne CRS projekta)
        layer_crs = layer.crs()
        is_epsg_3794 = False
        if layer_crs is not None and layer_crs.isValid():
            authid = (layer_crs.authid() or "").upper()
            if authid == "EPSG:3794":
                is_epsg_3794 = True
            else:
                try:
                    is_epsg_3794 = int(layer_crs.postgisSrid()) == 3794
                except Exception:
                    is_epsg_3794 = False

        if not is_epsg_3794:
            crs_label = "neznan CRS"
            if layer_crs is not None and layer_crs.isValid():
                crs_label = layer_crs.authid() or "neznan CRS"

            reply = QMessageBox.warning(
                self.iface.mainWindow(),
                "QNarcIS",
                f"Izbrani sloj ni v CRS EPSG:3794 (CRS izbranega sloja je: {crs_label}).\n"
                "Ali želite vseeno nadaljevati s pošiljanjem?",
                QMessageBox.Yes | QMessageBox.No,
                QMessageBox.No
            )
            if reply != QMessageBox.Yes:
                return

        # 2) Logika izbora + potrditve
        selected_only = True
        sel_count = len(layer.selectedFeatures())

        from .selection_confirm_dialog import SendSelectionDialog
        dlg = SendSelectionDialog(layer, sel_count, parent=self.iface.mainWindow())
        if dlg.exec_() != dlg.Accepted:
            return

        selected_only = dlg.selected_only
        fid_field_name = dlg.fid_field_name  # None => uporabi feature.id()

        # 3) Poverilnice (obstoječi pristop)
        uname, pwd, cfg = self.getCredentials()
        if not uname or not pwd:
            return
        
        self.saveCredentialsIfValidAsync(uname, pwd, cfg)

        # 4) Zagon naloge (QgsTask) – z blokado ponovnega zagona
        try:
            t = LayerUploadTask(
                layer,
                username=uname,
                password=pwd,
                description="NARCIS: pošlji izbrane objekte" if selected_only else "NARCIS: pošlji celoten sloj",
                selected_only=selected_only
            )

            setattr(t, "fid_field_name", fid_field_name)

            self._upload_running = True

            # Robustno: počisti zaklep, ko status doseže "Complete" ali več (velja tudi za "Canceled"/"Terminated")
            COMPLETE = getattr(QgsTask, "Complete", 3)

            def _maybe_clear(flag_status):
                try:
                    # flag_status je int (QgsTask.Status)
                    if int(flag_status) >= int(COMPLETE):
                        self._upload_running = False
                except Exception:
                    # če karkoli zataji, raje sprosti zaklep
                    self._upload_running = False

            try:
                t.statusChanged.connect(_maybe_clear)
                t.destroyed.connect(lambda *args, **kwargs: setattr(self, "_upload_running", False))
            except Exception:
                pass

            QgsApplication.taskManager().addTask(t)
            self._sending_task = t

        except Exception as e:
            self._upload_running = False
            QgsMessageLog.logMessage("Napaka pri zagonu naloge: {}".format(e), "QNarcIS", Qgis.Critical)
            self.iface.messageBar().pushMessage("QNarcIS", "Napaka pri zagonu naloge za pošiljanje sloja.", level=Qgis.Critical)

